function function1() {
    if (turn == 1) {
        if (box1.innerHTML == "") {
            box1.innerText = 'X';
            player_turn.innerHTML = "Player O's turn";
            turn++;
        } else {
            alert("Field is already marked.");
        }
    } else if (turn == 2) {
        if (box1.innerHTML == "") {
            box1.innerText = 'O';
            player_turn.innerHTML = "Player X's turn";
            turn--;
        } else {
            alert("Field is already marked.");
        }
    }
}
